export type TranslationKey = keyof typeof translations.zh

const current_locale_store = {
  locale: 'zh' as Locale,
  listeners: new Set<() => void>(),
  get(): Locale {
    return this.locale
  },
  set(locale: Locale) {
    this.locale = locale
    this.listeners.forEach((fn) => fn())
  },
  subscribe(fn: () => void): () => void {
    this.listeners.add(fn)
    return () => this.listeners.delete(fn)
  },
}

export function t(key: TranslationKey): string {
  return translations[current_locale_store.get()][key] ?? key
}

export function use_locale(): [Locale, (l: Locale) => void] {
  return [current_locale_store.get(), current_locale_store.set]
}

export { current_locale_store }

export function use_translation(): (key: TranslationKey) => string {
  return t
}
