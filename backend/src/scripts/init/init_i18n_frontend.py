class FrontendI18nInit:
    """前端国际化初始化"""

    @classmethod
    def run(cls) -> None:
        logger.info("=" * 60)
        logger.info("初始化前端国际化翻译数据")
        logger.info("=" * 60)

        db = get_database()
        session = db.get_session()
        try:
            saved_count = 0
            for locale, translations in FRONTEND_TRANSLATIONS.items():
                for key, value in translations.items():
                    # 从 key 中解析 namespace（格式为 "namespace.something"）
                    if "." in key:
                        ns_idx = key.find(".")
                        namespace = key[:ns_idx]
                        db_key = key[ns_idx + 1 :]
                    else:
                        namespace = "frontend"
                        db_key = key
                    existing = (
                        session.query(I18nStringModel)
                        .filter_by(t_locale=locale, t_namespace=namespace, t_key=db_key)
                        .first()
                    )
                    if existing:
                        existing.t_value = value
                    else:
                        model = I18nStringModel(
                            t_locale=locale,
                            t_namespace=namespace,
                            t_key=db_key,
                            t_value=value,
                        )
                        session.add(model)
                        saved_count += 1

            session.commit()
            logger.info(f"[OK] 前端翻译数据初始化完成, 共保存 {saved_count} 条记录")

        except Exception as e:
            session.rollback()
            logger.error(f"[ERROR] 前端翻译初始化失败: {e}")
            raise
        finally:
            session.close()


if __name__ == "__main__":
    FrontendI18nInit.run()
