def _flatten_dict(d: dict, prefix: str = "") -> dict[str, str]:
    """将嵌套 dict 扁平化为 {namespace.key.subkey: value}"""
    result: dict[str, str] = {}
    for k, v in d.items():
        full_key = f"{prefix}.{k}" if prefix else k
        if isinstance(v, dict):
            result.update(_flatten_dict(v, full_key))
        else:
            result[full_key] = v
    return result


def main() -> None:
    """主入口"""
    print("Initializing database connection...")
    db = get_database()
    session = db.get_session()

    try:
        repo = I18nRepository(session)

        # 统计
        total_zh = 0
        total_en = 0

        # 插入中文翻译
        print("Seeding zh translations...")
        for namespace, keys in ZH_TRANSLATIONS.items():
            flat = _flatten_dict(keys)
            for key, value in flat.items():
                repo.upsert(locale="zh", namespace=namespace, key=key, value=value)
                total_zh += 1
        print(f"  Inserted {total_zh} zh translations")

        # 插入英文翻译
        print("Seeding en translations...")
        for namespace, keys in EN_TRANSLATIONS.items():
            flat = _flatten_dict(keys)
            for key, value in flat.items():
                repo.upsert(locale="en", namespace=namespace, key=key, value=value)
                total_en += 1
        print(f"  Inserted {total_en} en translations")

        session.commit()
        print(f"Done! Total: {total_zh + total_en} translations ({total_zh} zh + {total_en} en)")

    except Exception as e:
        session.rollback()
        print(f"Error: {e}")
        raise
    finally:
        session.close()


if __name__ == "__main__":
    main()
